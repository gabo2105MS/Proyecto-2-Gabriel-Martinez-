    private void Aceptar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Aceptar1ActionPerformed
        validaciones validar = new validaciones();
        if (validar.validarNombreUsuario(nombreUser.getText())) {
            if(validar.tipoUsuario(TipoUser.getText().toLowerCase())){
                
                if(!hash.usuarioPresente(nombreUser.getText().toLowerCase())){
                    hash.insertar(nombreUser.getText(), TipoUser.getText());
                    JOptionPane.showMessageDialog(null, "EL usuario ha sido a√±adido con exito!!! ");
                   // hash.imprimir();
                    Inicio v3 = new Inicio(hash, c, cola);
                    v3.setVisible(true);
                    this.dispose();
                }
                else{
                    JOptionPane.showMessageDialog(null, "El usuario ya se encuentra dentro del sistema, intente con otro porfavor");
                }
                
            }
            else{
                 JOptionPane.showMessageDialog(null, "El formato del tipo de usuario es incorrecto \n Prueba el siguiente formato: 'prioridad_alta', 'prioridad_media' , 'prioridad_baja' \n Recuerda que son solo tres opciones");

            }
        } else {
            JOptionPane.showMessageDialog(null, "El nombre de usuario debe contener al menos un numero por ejemplo: 'pedrito123' y no debe contener espacios en blancos");
        }
        
    }//GEN-LAST:event_Aceptar1ActionPerformed

